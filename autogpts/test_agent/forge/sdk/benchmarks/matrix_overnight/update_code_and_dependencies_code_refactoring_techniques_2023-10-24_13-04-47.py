# Task assignment and delegation
def task_assignment(tasks, team_members):
    """
    Assigns tasks to team members and delegates tasks to other team members
    Args:
        tasks (list): List of tasks to be assigned
        team_members (list): List of team members
    Returns:
        dict: Dictionary mapping tasks to team members
    """
    # Assign tasks to team members
    task_assignments = dict.fromkeys(tasks)
    # Assign tasks to other team members
    for task, member in itertools.product(task_assignments, team_members):
        task_assignments[task] = member
    return task_assignments


